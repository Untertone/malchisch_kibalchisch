// Moves a node along a pre-saved .path file using WorldTransformPath.
// This is the simplest approach for path-following as WorldTransformPath
// handles all the interpolation and timing internally. The path file is
// loaded at runtime and the attached node automatically follows it with
// configurable speed and looping. Best for pre-authored paths.

#region Math Variables
#if UNIGINE_DOUBLE
using Scalar = System.Double;
using Vec2 = Unigine.dvec2;
using Vec3 = Unigine.dvec3;
using Vec4 = Unigine.dvec4;
using Mat4 = Unigine.dmat4;
#else
using Scalar = System.Single;
using Vec2 = Unigine.vec2;
using Vec3 = Unigine.vec3;
using Vec4 = Unigine.vec4;
using Mat4 = Unigine.mat4;
using WorldBoundBox = Unigine.BoundBox;
using WorldBoundSphere = Unigine.BoundSphere;
using WorldBoundFrustum = Unigine.BoundFrustum;
#endif
#endregion

using Unigine;

// Plays back a pre-saved path file using UNIGINE's built-in WorldTransformPath.
public partial class SavedPathTrajectory : Component
{
	// Path to the .path file generated by PathTrajectorySaver or other tools
	[ShowInEditor][ParameterFile]
	private string trajectoryFilePath = null;
	// Movement speed along the path (distance per second)
	[ShowInEditor]
	private float velocity = 10.0f;
	public float Velocity { get { return velocity; } set { velocity = value; } }
	// When true, draws the path as white lines
	[ShowInEditor]
	private bool debug;
	public bool Debug { get { return debug; } set { debug = value; } }

	// Built-in engine class that handles path playback automatically
	private WorldTransformPath transformPath;

	// Loads the path file and attaches this node to follow it.
	void Init()
	{
		node.WorldPosition = new Vec3(0, 0, 0);

		// Create path player and configure looping playback
		transformPath = new WorldTransformPath(trajectoryFilePath);
		transformPath.Loop = 1;      // Loop forever
		transformPath.Time = 0.0f;   // Start at beginning
		transformPath.Speed = velocity;
		transformPath.Play();
		// Attach node as child so it automatically follows the path
		transformPath.AddChild(node);
	}

	// Updates speed and optionally visualizes the path.
	void Update()
	{
		transformPath.Speed = velocity;

		if (debug)
			VisualizePath();
	}

	// Draws lines between path keyframes for debugging.
	private void VisualizePath()
	{
		Path path = transformPath.Path;
		int num_frames = path.NumFrames;
		for (int i = 0; i < num_frames; i++)
		{
			Vec3 curr_point = path.GetFramePosition(i);
			Vec3 next_point = path.GetFramePosition((i + 1) % num_frames);

			Visualizer.RenderLine3D(curr_point, next_point, vec4.WHITE);
		}
	}
}
